function t(key){
  var args=Array.prototype.slice.call(arguments,1);
  var lang=T[currentLang]||T.en;
  var val=lang[key]!==undefined?lang[key]:(T.en[key]||key);
  return typeof val==='function'?val.apply(null,args):val;
}

